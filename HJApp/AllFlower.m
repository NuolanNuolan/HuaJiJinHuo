#import "AllFlower.h"

@implementation AllFlower

+(id)getAllFlowerDictionary:(NSDictionary*)dic
{
    return [[self alloc]initWithDictionary:dic];
    
}
-(id)initWithDictionary:(NSDictionary*)dic
{
    self=[super init];
    if (self)
    {
        _flowerId=[dic[@"id"]copy];
        _name=[dic[@"name"]copy];
    }
    return self;
}
@end
